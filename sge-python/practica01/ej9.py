# Escribe un programa que calcule la superficie de un triángulo, para ello tiene que pedir la longitud de la base y
# de la altura:

print("Vamos a calcular la superficie de un triángulo...")
base = int(input("¿Cuánto mide de base (en cm): "))
altura = int(input("¿Cuánto mide de altura (en cm): "))
print(f"La superficie del triángulo es de {(base * altura) / 2} cm^2")
