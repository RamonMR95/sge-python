# Escribe un programa que pregunte por tu nombre, por tu primer apellido y por tu segundo apellido.

nombre = input("¿Cuál es tu nombre?")
primer_apellido = input("¿Y tu primer apellido?")
segundo_apellido = input("¿Y tu segundo apellido?")
print(f"Te llamas: {nombre} {primer_apellido} {segundo_apellido}")
print(f"En las listas aparecerías como: {primer_apellido} {segundo_apellido}, {nombre}")
